;// Draw Tile ;//
;//-----------;//


;// draw an 8x8 tile from a data pointer
;// r1 = color 1
;// r2 = color 2
;// r3 = x (to screen)
;// r4 = y (to screen)
;// r5 = tile number
DrawTile:
	;// registers reference:
	;// r1 = reserved (plot color)
	;// r2 = reserved (plot x)
	;// r3 = reserved (plot y)
	;// r4 = color 1
	;// r5 = color 2
	;// r6 = x
	;// r7 = y
	;// r8 = loop1 (row)
	;// r9 = loop2 (column)
	;// r10 = bitmask
	;// r11 = graphics byte

	;// save the return address
	;//lr	K, P
	;//pi	pushk

	;// get the tile address
	dci	tiles
	;// add the offset
	lr	A, 5
	inc							;// make sure we hit 0
	lr	0, A
	lis	2						;// three bytes for each tile
.drawTileAddressLoop:
	ds	0
	bz	.drawTileAddressLoopEnd
	adc
	br	.drawTileAddressLoop
.drawTileAddressLoopEnd:
	;// got the tile data, now get the graphics address
	lm
	lr	Qu, A
	lm
	lr	Ql, A
	lr	DC, Q

	;// Get the colour of the char

	;// load the width and height
	li	13
	lr	5, A
	li 	13
	lr	6, A

.drawTileBlit:
	;// draw the tile itself
	jmp	blit



DrawChar:
	;// registers reference:
	;// r1 = reserved (plot color)
	;// r2 = reserved (plot x)
	;// r3 = reserved (plot y)
	;// r4 = color 1
	;// r5 = color 2
	;// r6 = x
	;// r7 = y
	;// r8 = loop1 (row)
	;// r9 = loop2 (column)
	;// r10 = bitmask
	;// r11 = graphics byte

	;// save the return address
	;//lr	K, P
	;//pi	pushk

	;// get the tile address
	dci	chars
	;// add the offset
	lr	A, 5
	inc							;// make sure we hit 0
	lr	0, A
	lis	2						;// three bytes for each tile
.drawCharAddressLoop:
	ds	0
	bz	.drawCharAddressLoopEnd
	adc
	br	.drawCharAddressLoop
.drawCharAddressLoopEnd:
	;// got the tile data, now get the graphics address
	lm
	lr	Qu, A
	lm
	lr	Ql, A
	lr	DC, Q

	;// Get the colour of the char

	;// load the width and height
	li	5
	lr	5, A
	li 	5
	lr	6, A

.drawCharBlit:
	;// draw the tile itself
	jmp	blit




	





